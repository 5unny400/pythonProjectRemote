# 例子
my_list = ['apple', 'banana', 'orange']

for index, value in enumerate(my_list):
    print(f"Index: {index}, Value: {value}")

# 输出:
# Index: 0, Value: apple
# Index: 1, Value: banana
# Index: 2, Value: orange


